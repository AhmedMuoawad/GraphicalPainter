    private void DrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DrawActionPerformed
        String xx1, yy1, xx2, yy2;  
        

         xx1=  (Draw_x1.getText());
         yy1 = (Draw_y1.getText());
         xx2 = (Draw_x2.getText());
         yy2 = (Draw_y2.getText());
         if(xx1.length()!=0 && yy1.length()!=0&&xx2.length()!=0&&yy2.length()!=0){
             
         float x1, y1, x2, y2, dx, dy; 
         float m;
        int k;
         x1=  Float.parseFloat(Draw_x1.getText());
         y1 = Float.parseFloat(Draw_y1.getText());
         x2 = Float.parseFloat(Draw_x2.getText());
         y2 = Float.parseFloat(Draw_y2.getText());
        dx = x2 - x1;
        dy = y2 - y1;
        m = dy / dx;
        d.paintline(x1+364, x2+364,293- y1, 293-y2);
        d.setVisible(true);

        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        if (m < 1) {
            k = 0;
            for (int i = (int) x1; i <= x2; i++, k++, y1 += m) {
                model.addRow(new Object[]{k, i, y1, i, Math.round(y1)});
            }
        } else {
            k = 0;
            for (int i = (int) y1; i <= y2; i++, k++, x1 += (1 / m)) {
                model.addRow(new Object[]{k, x1, i, Math.round(x1), i});
            }
        }
                    }else
                        JOptionPane.showMessageDialog(null, "You should enter all varible ");
    }//GEN-LAST:event_DrawActionPerformed

