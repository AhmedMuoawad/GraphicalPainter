    private void RotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RotActionPerformed
        
        float x1, y1, x2, y2, endX,endY;
        
        double angle = Math.toRadians(Double.parseDouble(Angle.getText()));
        double sin = Math.sin(angle);
        double cos = Math.cos(angle);
        String cetaCheck =(Angle.getText());
        if(cetaCheck.length()!=0)
        {
         x1 = Integer.parseInt(Draw_x1.getText());
        y1 = Integer.parseInt(Draw_y1.getText());
        x2 = Integer.parseInt(Draw_x2.getText());
        y2 = Integer.parseInt(Draw_y2.getText());

        endX = (int) (x2 * cos - y2 * sin);
        endY = (int) (x2 * sin + y2 * cos);
        r.rotate(x1+364,293-y1, 364+endX,293-endY);
        jPanel2.add(r);

        
        
        
        }
    }//GEN-LAST:event_RotActionPerformed

